package SwappingProgram;

public class swappingOfTwoNumber {
public static void main(String[] args) {
	int a=20, b=30;
	System.out.println("Before swapping value is=> "+ "a=>"+a+" "+"b=>"+b);
	// After swapping
	
		int t;
		t=a;
		a=b;
		b=t;
		
		System.out.println("After swapping value is=> "+ "a=>"+a+" "+"b=>"+b);
		// After swapping
}
}
